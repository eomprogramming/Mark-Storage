package com.earlofmarch.mathMarks.model;

/**
 * A type, sort or purpose of a {@link Mark}.
 * @author Ian Dewan
 */
public enum MarkType {
	/**
	 * Marks based on _actual work_.
	 */
	REAL_MARKS,
	
	/**
	 * Marks based on how you "interact with the teacher," and "expand your learning"
	 * by "asking questions."
	 */
	CONVERSATIONAL,
	
	/**
	 * Marks based observing the student working.
	 */
	OBSERVATIONAL
}
